class LLNode {

	public LLNode(int elem, LLNode next) {
		elem_ = elem;
		next_ = next;
	}

	public LLNode(int elem) {
		elem_ = elem;
		next_ = null;
	}

	public int elem() {
		return elem_;
	}

	public LLNode next() {
		return next_;
	}

	public void setElem(int elem) {
		elem_ = elem;
	}

	public void setNext(LLNode next) {
		next_ = next;
	}

	private int elem_;
	private LLNode next_;

}
